<script setup lang="ts">
import type { TableProps } from 'antdv-next'
import { computed, ref } from 'vue'

interface DataType {
  key: string
  name: string
  age: number
  address: string
}

const dataSource: DataType[] = [
  {
    key: '1',
    name: 'John Brown',
    age: 32,
    address: 'New York No. 1 Lake Park',
  },
  {
    key: '2',
    name: 'Jim Green',
    age: 42,
    address: 'London No. 1 Lake Park',
  },
  {
    key: '3',
    name: 'Joe Black',
    age: 32,
    address: 'Sydney No. 1 Lake Park',
  },
  {
    key: '4',
    name: 'Disabled User',
    age: 99,
    address: 'Sydney No. 1 Lake Park',
  },
]

const columns: TableProps['columns'] = [
  { title: 'Name', dataIndex: 'name', key: 'name' },
  { title: 'Age', dataIndex: 'age', key: 'age' },
  { title: 'Address', dataIndex: 'address', key: 'address' },
]

const selectionType = ref<'checkbox' | 'radio'>('checkbox')

const selectedRowKeys = ref<Array<string | number>>([])
const rowSelection = computed<TableProps['rowSelection']>(() => ({
  type: selectionType.value,
  selectedRowKeys: selectedRowKeys.value,
  onChange: (_selectedRowKeys, selectedRows) => {
    console.log(`selectedRowKeys: ${_selectedRowKeys}`, 'selectedRows: ', selectedRows)
    selectedRowKeys.value = _selectedRowKeys
  },
  getCheckboxProps: record => ({
    disabled: record.name === 'Disabled User',
    name: record.name,
  }),
}))
</script>

<template>
  <div>
    <a-radio-group v-model:value="selectionType">
      <a-radio value="checkbox">
        Checkbox
      </a-radio>
      <a-radio value="radio">
        radio
      </a-radio>
    </a-radio-group>
    <a-divider />
    <a-table :columns="columns" :data-source="dataSource" :row-selection="rowSelection">
      <template #bodyCell="{ column, text }">
        <template v-if="column.key === 'name'">
          <a>{{ text }}</a>
        </template>
      </template>
    </a-table>
  </div>
</template>
